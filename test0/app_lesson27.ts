
function doubleTypeFunction(a: number | string){
    if(typeof a === 'string'){
        return a.replace('x', 'X');
    }

    //어떤 데이터 유형이 데이터 유형에 맞는 변수에 대입되는지만 보는 것이 아니라 코드 흐름까지 분석하는 예
    return a.replace('Y', 'y');
}

doubleTypeFunction(10);

